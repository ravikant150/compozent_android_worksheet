void main() {
  // Initialize variables
  int a = 10;
  int b = 5;

  // Perform operations
  int sum = a + b;
  int difference = a - b;
  int product = a * b;
  double quotient = a / b; // Division returns a double

  // Print the results
  print('Sum of $a and $b: $sum');
  print('Difference of $a and $b: $difference');
  print('Product of $a and $b: $product');
  print('Quotient of $a and $b: $quotient');
}